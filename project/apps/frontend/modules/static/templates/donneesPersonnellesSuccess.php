              <p>Interdiction des traitements visant à la réidentification des justiciables :</p>

              <p>Les données présentes sur le site www.juricaf.org sont pseudonymisées conformément aux articles L 111-13 et suivants du code de l’organisation judiciaire français sauf à respecter la législation d’origine des décisions et les principes fondamentaux auxquels obéissent les juridictions étrangères. C’est pour cette raison que les décisions de la Cour suprême de Canada et de la Cour Européenne des droits de l’Homme ne font pas l’objet d’une pseudonymisation.</p>

              <p>La réutilisation des données de la base www.juricaf.org ne peut avoir pour objet ou pour effet de réidentifier les personnes concernées, ce que rappelle la CNIL et Etalab en utilisant la formulation suivante s’agissant de la réutilisation des données de jurisprudence :</p>

              <p>« Dans le cadre de leurs missions de service public, les administrations produisent ou reçoivent des informations publiques qui peuvent être réutilisées par toute personne physique ou morale à d’autres fins que celles de la mission de service public. Lorsque ces informations contiennent des données à caractère personnel, c’est-à-dire des éléments qui permettent d’identifier, directement ou indirectement, une personne physique, leur réutilisation est étroitement encadrée par l’article L322-2 du code des relations entre le public et l’administration. Cet article prévoit que la réutilisation d’une information publique contenant des données à caractère personnel est subordonnée au respect de la loi n° 78-17 du 6 janvier 1978, dite « Informatique et libertés ». Il en résulte notamment que lorsque les données personnelles que cette information publique contient ont, préalablement à leur diffusion, fait l’objet d’une anonymisation totale ou partielle, conformément à des dispositions légales ou aux recommandations de la Commission nationale de l’informatique et des libertés (CNIL), la réutilisation ne peut avoir pour objet ou pour effet de réidentifier les personnes concernées. »</p>

              <p>Interdiction des traitements visant à la réidentification des magistrats et greffiers :</p>

              <p>L’article L 111-13 du COJ dispose que « Les données d'identité des magistrats et des membres du greffe ne peuvent faire l'objet d'une réutilisation ayant pour objet ou pour effet d'évaluer, d'analyser, de comparer ou de prédire leurs pratiques professionnelles réelles ou supposées. La violation de cette interdiction est punie des peines prévues aux articles 226-18,226-24 et 226-31 du code pénal, sans préjudice des mesures et sanctions prévues par la loi n° 78-17 du 6 janvier 1978 relative à l'informatique, aux fichiers et aux libertés. »</p>


