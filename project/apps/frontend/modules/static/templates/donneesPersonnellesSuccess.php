              <p>Les données des juridictions françaises sont issues des bases de données Judilibre et Ariane. La n° 2019-222 du 23 mars 2019 de programmation 2018-2022 et de réforme pour la justice et ses textes d’applications organisent la réutilisation de ces données.</p>

