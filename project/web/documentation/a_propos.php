              <h1>À propos</h1>
              
              <p>La base JURICAF est destinée en priorité au monde francophone, mais aussi aux ressortissants des pays de Common Law afin qu’ils puissent avoir accès à la jurisprudence rendue en français.</p>
              
              <p>Cette base de jurisprudence appartient à l’<a href="https://www.ahjucaf.org/">association AHJUCAF</a> et résulte de la contribution régulière des Cours suprêmes adhérentes de l’AHJUCAF et pratiquant l’usage du français.</p>
              
              <p>Sous la responsabilité du Secrétaire général de l’AHJUCAF, le fonctionnement et l’alimentation du site sont assurés par une équipe animée par un greffier de la Cour de cassation française qui veille à son alimentation régulière. Un webmestre assure l’hébergement sécurisé des sites JURICAF et AHJUCAF.</p>

              <p>Depuis septembre 2016, M. Jean-Paul Jean Président de chambre honoraire à la Cour de cassation de France assure les fonctions de Secrétaire général de l’AHJUCAF. Une réorganisation des processus de récupération et de traitement des décisions a été conduite avec le concours de partenaires attachés depuis l’origine au projet JURICAF, les animateurs des sociétés 24ème et La Loi des Ours, ainsi que l’éditeur Lexbase. La reprise de l’alimentation régulière et la stratégie de développement ont permis à JURICAF de dépasser au cours de l’année 2020 le million d’arrêts intégrés. </p>

              <p>Dans le même temps JURICAF a été mis aux normes RGPD. La quasi-totalité de ses fonds sont désormais pseudonymisés, sauf pour quelques situations très particulières de Cours suprêmes non soumises à cette exigence (voir la page <a href="mentions_legales.php">Mentions légales</a>)</p>

              <p>Les arrêts des Cours suprêmes de pays francophones de l’hémisphère nord (France, Belgique, Luxembourg, Canada) sont intégrés automatiquement tous les mois. Début 2021 le fonds des arrêts CEDH a été mis à jour avec les décisions de 2013 à 2021.</p>

              <p>Plus de la moitié des Cours suprêmes d’Afrique francophone font parvenir leurs décisions au secrétariat de l’AHJUCAF, de manière plus régulière et sous un format normalisé. D’autres pays membres de l’AHJUCAF dont les arrêts ne sont pas rendus en français (par exemple l’Albanie, la Bulgarie, la Roumanie, les pays de langue arabe) transmettent régulièrement des arrêts traduits. Des séminaires de formation sont organisés avec les correspondants de l’AHJUCAF, facilitant ainsi la transmission régulière de jurisprudences sous une forme compatible.</p>


              <h1>Histoire de JURICAF</h1>
              
              <p>JURICAF est un projet de l'AHJUCAF, l'association des Cours judiciaires suprêmes francophones, réalisé initialement en partenariat avec le Laboratoire Normologie Linguistique et Informatique du droit (Université Paris I), avec le soutien de l'Organisation internationale de la Francophonie.</p>
              <p>L’AHJUCAF a décidé de mettre à disposition des professionnels du droit et des citoyens de l’espace judiciaire francophone une base de jurisprudence permettant un accès gratuit aux décisions de justice rédigées ou traduites en français. La diffusion du droit francophone vient ainsi renforcer l’État de droit et la démocratie.</p>
              <p>A la fin des années 2000, les États francophones avaient pris un retard important dans le développement de bases de données juridiques disponibles sur internet. Si en Europe, la plupart des hautes juridictions disposaient d’un tel outil, dans les pays d'Afrique, Caraïbes et Pacifique, aucune institution ne diffusait gratuitement sa jurisprudence, contrairement aux pays de Common Law où les décisions de justice des Final Court of Appeal étaient déjà accessibles sur Internet.</p>
              <p>Nombre d’avocats, de citoyens, de défenseurs des droits de l’homme, n’avaient pas accès aux décisions de justice et ne pouvaient connaître le verdict d’affaires jugées. Dans le domaine économique, nombre d’arrêts de la Cour Commune de Justice et d’Arbitrage de l’OHADA étaient accessibles, mais avec une mise à jour incertaine et une publication ne répondant pas aux normes documentaires, retardant ainsi leur exploitation par les avocats.</p>
              <p>En juin 2010, après six ans d’existence sur Internet, l’Assemblée générale de l’AHJUCAF a décidé une remise à niveau ambitieuse sur le plan documentaire et technique de sa base de données JURICAF qui respecte les standards mondiaux, européens et francophones.</p>
              <p>L’AHJUCAF soutient et accompagne désormais les services de documentation des Cours suprêmes africaines pour qu’elles se dotent progressivement des technologies informatiques et documentaires et permettre ainsi la diffusion de leur jurisprudence à l’échelle mondiale.</p>
              <p>Le VIème Congrès de l’AHJUCAF tenu à Beyrouth les 13-14 juin 2019 sur le thème « <a href="https://www.ahjucaf.org/sites/default/files/inline-files/AHJUCAF%20Congre%CC%80s%20Beyrouth%20Actes%20sommaire%20mars%202020_0.pdf">Recommandations adoptées à Beyrouth par les Cours suprêmes judiciaires de la Francophonie</a>, au nom de leurs valeurs communes et au moment de l’ouverture croissante des données publiques, ont fixé comme action prioritaire la diffusion par internet de la jurisprudence francophone numérisée, afin de faciliter les échanges de la communauté internationale des juristes et le développement de l’Etat de droit, au service des citoyens pour faciliter la connaissance et l’exercice effectifs de leurs droits dans une société démocratique.</p>
              
              <p>Télécharger l’ouvrage «&nbsp;<a href="https://www.ahjucaf.org/sites/default/files/inline-files/AHJUCAF%20Congre%CC%80s%20Beyrouth%20Actes%20mars%202020.pdf">La diffusion de la jurisprudence des Cours suprêmes judiciaires dela francophonie au temps d’internet</a>&nbsp;»</p>

